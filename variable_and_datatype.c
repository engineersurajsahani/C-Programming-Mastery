#include <stdio.h>

int main()
{
    // Variable :- It is like a container in which we can store some value.
    
    // Data Type :- It define the type of the value or data which are going to be stored in the variable.
    
    int a=5; // integer literal --> %d
    float f=5.5;// floating point literal --> %f 
    char c='s';// character literal --> %c 
    
    printf("%d",a);
    printf("\n%.0f",f);
    printf("\n%c",c);
    
    printf("\n\n%d  %f %c",a,f,c);

    return 0;
}
